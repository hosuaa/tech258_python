# Operators

x = 5
y = 1
print("x =", x)
print("y =", y)

# arithmetic operators - +,-,*,/,%
result = x + y + 2
print("x+y+2 =", result)
result = x - y - 2
print("x-y-2 =", result)
result = x * y * 2
print("x*y*2 =", result)
result = x / y / 2
print("x/y/2 =", result)
result = x % y % 2
print("x%y%2 =", result)

# comparison operators - <,>,==,!=,>=,<=
result = x < y
print("x<y =", result)
result = x > y
print("x>y =", result)
result = x == y
print("x==y =", result)
result = x != y
print("x!=y =", result)
result = x >= y
print("x>=y =", result)
result = x <= y
print("x<=y =", result)
